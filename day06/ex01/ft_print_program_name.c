#include <unistd.h>

void	ft_putchar(char c);

void	print_filename(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	print_filename(argv[0]);
	return (0);
}
