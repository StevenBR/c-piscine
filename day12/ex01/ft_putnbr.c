#include <unistd.h>
#include "cathead.h"

void	ft_putnbr(int nb)
{
	char neg;

	neg = '-';
	if (nb < 0)
	{
		write(1, &neg, 1);
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	ft_putchar(nb % 10 + 48);
}
