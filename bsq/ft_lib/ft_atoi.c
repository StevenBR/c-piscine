int	ft_atoi(char *str)
{
	int		i;
	long	num;
	int		sign;

	num = 0;
	i = 0;
	sign = 1;
	while (str[i] == '\0' || str[i] == '\t' || str[i] == ' ')
	{
		i++;
	}
	if (str[0] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] != '\0' && str[i] <= '9' && str[i] >= '0')
	{
		num = (num * 10) + str[i] - '0';
		i++;
	}
	return (sign * num);
}
